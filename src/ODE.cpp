void cellType1_ODEs::ODE_system(const cellType1_ODEs::cellType1_ode_states &x, cellType1_ODEs::cellType1_ode_states &dxdt, double t) {
// Species Definitions //
    double A = x[0];
    double B = x[1];
// Parameter Definitions //
// Rate Definitions //
    double rate_k1 = calc_k1_rate(A);
// ODEs Definitions //
    dxdt[0] = -rate_k1*1; // Rate of change for species A
    dxdt[1] = +rate_k1*1; // Rate of change for species B
}

